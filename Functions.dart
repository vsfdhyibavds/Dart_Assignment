// Functions in Dart
// 1. Addition
int addTwo(int a, int b){
    int sum = a + b;
    return sum;
}

// 2. Subtraction
int subtractTwo(int a, int b){
    int subtract = a - b;
    return subtraction;
}

// 3. Multiplication
int multiplyTwo(a * b){
    int multiply = a * b;
    return multiplication;
}

// 4. Division
int divideTwo(a / b){
    int divide = a / b;
    return division;
}

// 5. StringLength
int stringLength(String str){
    return str.Length;
}

// 6. GetFirstLength
dynamic getFirstElement(List<dynamic> List){
    if(List.isNotEmpty){
        return list[0];
    } else {
        return null;
    }
}